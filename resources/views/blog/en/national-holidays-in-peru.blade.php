                                    <h3>La Bandera del Perú</h3>
                                    <p>
                                        Este emblema es una banda rectangular formado por 3 colores que van en vertical
                                        y con un ancho igual, tiene rojo en ambos lados y en el centro el color blanco.
                                        El color rojo simboliza la sangre derramada por miles de peruanos, héroes y
                                        mártires que dieron la vida para defender los intereses de la patria y a sus
                                        habitantes.<br><br>
                                        La primera bandera nacional fue creada por el libertador Don José de San Martín
                                        y fue desplegada por primera vez el 28 de julio de 1821 al momento de declarar
                                        la independencia de Perú. A lo largo de la historia tubo varias modificaciones y
                                        en la actualidad solo se han conservado los colores rojo y blanco en ella.
                                    </p>
