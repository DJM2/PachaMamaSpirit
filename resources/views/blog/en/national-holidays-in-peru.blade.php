                                    <h3>The National emblem</h3>
                                    <p>
                                        It is a heraldic symbol and is an important national emblem that was created by
                                        Don José de San Martín in Pisco on October 21, 1820, it is the symbol of the
                                        sovereignty of Peru. In 1825 the constituent congress ordered that the new
                                        national coat of arms be divided into 3 large fields, which represent the riches
                                        of Peru in the 3 natural kingdoms that are: animal, vegetable and mineral. This
                                        is how on the upper right side is the cinchona tree, which represents the
                                        Peruvian flora. On its side (upper left), is the vicuña, which is the
                                        representative animal of Peru. Finally, at the bottom, below both symbols
                                        mentioned above, is the cornucopia spilling gold coins, thus representing the
                                        mining of the country.
                                    </p>
