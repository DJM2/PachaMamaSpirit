                                    <h3>El Escudo Nacional</h3>
                                    <p>
                                        Es un símbolo heráldico y es un importante emblema nacional que fue creado por
                                        Don José de San Martín en Pisco el 21 de octubre de 1820, es el símbolo de la
                                        soberanía de Perú. En 1825 el congreso constituyente, dispuso que el nuevo
                                        escudo nacional se divida en 3 grandes campos, que representen las riquezas de
                                        Perú en los 3 reinos naturales que son: animal, vegetal y mineral. Es así como
                                        en el lado superior derecho se encuentre el árbol de la quina, que representa la
                                        flora peruana, A su costado (superior izquierdo), se encuentra la vicuña que es
                                        el animal representativo de Perú. Por último, en la parte inferior, debajo de
                                        ambos símbolos mencionados anteriormente, se encuentra la cornucopia derramando
                                        monedas de oro, representando así la minería del país.
                                    </p>
