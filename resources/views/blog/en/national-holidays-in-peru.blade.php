                                    <h3>National anthem of Peru</h3>
                                    <p>
                                        The national anthem of Peru was written by José de la Torre Ugarte and composed
                                        by José Bernardo Alcedo in 1821 and had the title 'National March of Peru', it
                                        is a musical poetic composition of 7 stanzas and a chorus, all related to
                                        libertarian deeds. Sung for the first time on September 23, 1821 by Rosa Merino.
                                        <br><br>
                                        The current version of the national anthem of Peru was adopted by the musician
                                        Claudio Rebagliati in 1869. There was a demand by congressmen of the republic in
                                        2004 towards the first verse (A long time the oppressed Peruvian / The ominous
                                        chain dragged), since they considered apocryphal and that it contained phrases
                                        that damaged the dignity of Peruvians. The Constitutional Court declared that
                                        the first stanza was not authored by José de la Torre, but rather a product of
                                        national folklore and that it would remain intact since it represented the will
                                        of the people.
                                    </p>
