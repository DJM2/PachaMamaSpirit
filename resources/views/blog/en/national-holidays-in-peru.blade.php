                                    <h3>The Flag of Peru</h3>
                                    <p>
                                        This emblem is a rectangular band formed by 3 colors that go vertically and with
                                        an equal width, it has red on both sides and white in the center. The red color
                                        symbolizes the blood shed by thousands of Peruvians, heroes and martyrs who gave
                                        their lives to defend the interests of the country and its inhabitants.<br><br>

                                        The first national flag was created by the liberator Don José de San Martín and
                                        was displayed for the first time on July 28, 1821 at the time of declaring the
                                        independence of Peru. Throughout history it had several modifications and
                                        currently only the red and white colors have been preserved on it.
                                    </p>
                                    <img src="{{ asset('img/blog/bandera-nacional-del-Peru.webp') }}"
